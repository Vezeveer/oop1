    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        //Reset list
        if(tableModel != null)
            tableModel.setRowCount(0);
        
        boolean idSelected = false;
        String searchTag = "firstname";
        
        //Check if ID field is empty
        String fName = tfSearch.getText();
        if(fName.isEmpty()){
            JOptionPane.showMessageDialog(this, "field cannot be empty");
            return;
        }
        
        switch(cbSearchTag.getSelectedItem().toString()){
            case "ID": 
                searchTag = "id";
                idSelected = true;
                try{
                    Integer.parseInt(fName);
                } catch (Exception e){
                    JOptionPane.showMessageDialog(this, "ID must be a number");
                    return;
                }
                break;
            case "First Name": searchTag = "firstname"; break;
            case "Last Name": searchTag = "lastname"; break;
            case "City": searchTag = "city"; break;
            case "Street": searchTag = "street"; break;
        }
        
        try {
            ResultSet rs = stmt.executeQuery("select * from PEOPLE where " +
                    searchTag  + (idSelected ? 
                            ("="+fName)
                            :
                            (" LIKE '" + fName +"%'")
                            ));
            
            if(!rs.next()){
                JOptionPane.showMessageDialog(this, "No Record Found!");
                btnGoToProfile.setEnabled(false);
                btnGoToUpdate.setEnabled(false);
                btnDelete.setEnabled(false);
            } else {
                do{
                    
                    tableModel = (DefaultTableModel) tblSearch.getModel();
                    tableModel.insertRow(tableModel.getRowCount(), new Object[]{
                            Integer.parseInt(rs.getString("id")),
                            rs.getString("firstname"),
                            rs.getString("lastname"),
                            rs.getString("city"),
                            rs.getString("street"),
                            rs.getString("avatar")
                    });
                }while(rs.next());
            }
            rs.close();
        } catch (SQLException ex) {
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnSearchActionPerformed

