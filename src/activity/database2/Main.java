    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        
        try{
            Integer.parseInt(tfIDCreate.getText());
        } catch (Exception e){
            JOptionPane.showMessageDialog(this, "ID must be a number");
            return;
        }
        
        try {
            String id = tfIDCreate.getText();
            String lastName = tfLastNameCreate.getText();
            String firstName = tfFirstNameCreate.getText();
            String street = tfStreetCreate.getText();
            String city = tfCityCreate.getText();
            String avatar = lblAvatarCreate.getIcon().toString();
            
            avatar = avatar.substring(avatar.length()-8, avatar.length()-4);
            
            if (id.isEmpty() || lastName.isEmpty() || firstName.isEmpty()
                    || street.isEmpty() || city.isEmpty()){
                JOptionPane.showMessageDialog(this, "Please fill up all fields");
                return;
            }
            
            
            
            ResultSet rs = null;
            rs = stmt.executeQuery("select * from PEOPLE where id = " + id);
            
            if(!rs.next()){
                String sq1 = "insert into PEOPLE (id, firstname, lastname, "+
                        "city, street, avatar) VALUES (?, ?, ?, ?, ?, ?)";
                PreparedStatement statement = con.prepareStatement(sq1);
                statement.setString(1, id);
                statement.setString(2, firstName);
                statement.setString(3, lastName);
                statement.setString(4, city);
                statement.setString(5, street);
                statement.setString(6, avatar);
                
                System.out.println("Executing...");
                int rowsInserted = statement.executeUpdate();
                if (rowsInserted > 0){
                    JOptionPane.showMessageDialog(this, "Record added");
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnSubmitActionPerformed

